console.log("Difference between let,var and const");
var num=10;//VAR DECLARATION AND INITIALIZATION
console.log(num);
var num;// VAR RE-DECLARATION
console.log(num);
var num=20// VAR RE-DECLARATION AND RE-INITIALIZATION
console.log(num);
let num2=1;//IN let INITIALIZATION AND DECLARATION IS POSSIBLE
console.log(num2);
 num2=10;//IN let RE-INITIALIZATION IS ONLY POSSIBE (RE-DECLARATION IS NOT POSSIBLE)
console.log(num2);
const num1=10;//IN CONST DECLARATION AND INITIALIZATION IS ONLY POSSIBLE(IN SAME LINE)
console.log(num1);

